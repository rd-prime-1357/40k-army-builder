def read_csv(filepath):
    """Read a CSV file and return a list of dicts (one per row)."""
    rows = []
    try:
        with open(filepath, encoding="utf-8-sig") as f:
            reader = csv.DictReader(f)
            for row in reader:
                rows.append(row)
    except FileNotFoundError:
        print(f"ERROR: CSV file not found: {filepath}")
        sys.exit(1)
    return rows


def weapon_base(name):
    """Normalize a weapon name to its family base for matching: lowercase,
    drop any ' - profile' suffix, collapse whitespace. Used to detect when a
    flat wargear swap targets a family a bundle already manages."""
    if not name:
        return ""
    s = str(name).lower()
    s = re.split(r"\s+[\u2013\u2014-]\s+", s)[0]
    return " ".join(s.split())


def clean(value):
    """
    Convert a CSV cell value to the appropriate Python type.
    - Empty string / whitespace-only -> None
    - "Yes" / "No" -> True / False
    - Numeric strings -> int (if no decimal) or leave as str for fields
      that intentionally store dice expressions (handled per-field below)
    - Fixes backslash-escaped inch marks (6\\" or trailing 6\\) -> 6"
    """
    if value is None:
        return None
    v = value.strip()
    if v == "":
        return None
    if v.lower() == "yes":
        return True
    if v.lower() == "no":
        return False
    # Fix backslash-escaped inch marks from Excel CSV export
    v = v.replace('\\"', '"')
    if v.endswith('\\'):
        v = v[:-1] + '"'
    # Fix UTF-8 em-dash/en-dash misread as latin1 (â\x80\x93 -> -)
    v = v.replace('â', ' - ').replace('â', ' - ')
    # Collapse any double spaces introduced
    import re as _re
    v = _re.sub(r'  +', ' ', v).strip()
    return v


def to_int(value):
    """Convert to int, or None if blank."""
    v = clean(value)
    if v is None:
        return None
    try:
        return int(v)
    except (ValueError, TypeError):
        return v  # Return as string if not cleanly an int (safety fallback)


def split_list(value):
    """
    Convert a comma-separated string to a list of stripped strings.
    Returns an empty list if the value is blank.
    """
    v = clean(value)
    if v is None:
        return []
    return [item.strip() for item in v.split(",") if item.strip()]


def make_key(row, fields):
    """Build a tuple key from specified field names."""
    return tuple(clean(row.get(f, "")) for f in fields)


