def build_keywords(rows):
    """
    Simple lookup: list of { keyword_name, keyword_description }
    """
    result = []
    for row in rows:
        name = clean(row.get("Keyword Name"))
        desc = clean(row.get("Keyword Description"))
        if name:
            result.append({
                "keyword_name": name,
                "keyword_description": desc
            })
    return result


