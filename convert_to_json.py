def build_units(data):
    """
    Join all unit-related tabs on Army Name + Unit Name + Model Group
    and produce the nested structure defined in D26.
    """

    stats_rows      = data["unit_stats"]
    weapons_rows    = data["unit_weapons"]
    wargear_rows    = data["unit_wargear"]
    points_rows     = data["unit_points"]
    abilities_rows  = data["unit_abilities"]
    other_rows      = data["unit_other"]
    bundles         = data.get("bundles", {})

    # ------------------------------------------------------------------
    # Index supporting tabs by (Army Name, Unit Name, Model Group)
    # Points tab uses only (Army Name, Unit Name)
    # ------------------------------------------------------------------

    # Weapons: one list per (army, unit, model_group)
    weapons_by_key = defaultdict(list)
    for row in weapons_rows:
        key = make_key(row, ["Army Name", "Unit Name", "Model Group"])
        weapons_by_key[key].append(row)

    # Wargear options
    wargear_by_key = defaultdict(list)
    for row in wargear_rows:
        key = make_key(row, ["Army Name", "Unit Name", "Model Group"])
        wargear_by_key[key].append(row)

    # Other options
    other_by_key = defaultdict(list)
    for row in other_rows:
        key = make_key(row, ["Army Name", "Unit Name", "Model Group"])
        other_by_key[key].append(row)

    # Abilities
    abilities_by_key = defaultdict(list)
    for row in abilities_rows:
        key = make_key(row, ["Army Name", "Unit Name", "Model Group"])
        abilities_by_key[key].append(row)

    # Points: keyed by (army, unit) only
    points_by_key = {}
    for row in points_rows:
        key = make_key(row, ["Army Name", "Unit Name"])
        points_by_key[key] = row

    # ------------------------------------------------------------------
    # Group stats rows by army, then by unit
    # ------------------------------------------------------------------

    # armies_dict: { army_name: { unit_name: [stat_rows] } }
    armies_dict = defaultdict(lambda: defaultdict(list))
    for row in stats_rows:
        army = clean(row.get("Army Name"))
        unit = clean(row.get("Unit Name"))
        if army and unit:
            armies_dict[army][unit].append(row)

    # ------------------------------------------------------------------
    # Build output per army
    # ------------------------------------------------------------------

    output = []

    for army_name, units_dict in armies_dict.items():
        army_obj = {
            "army": army_name,
            "units": []
        }

        for unit_name, stat_rows in units_dict.items():

            # unit_type is the same across all model groups — take from first row
            unit_type = clean(stat_rows[0].get("Unit Type"))

            # ----------------------------------------------------------
            # model_groups
            # ----------------------------------------------------------
            model_groups = []
            for row in stat_rows:
                mg = clean(row.get("Model Group"))
                mg_key = (clean(row.get("Army Name")), unit_name, mg)

                model_group_obj = {
                    "model_group":              mg,
                    # Stats
                    "M":                        clean(row.get("M")),
                    "T":                        to_int(row.get("T")),
                    "SV":                       to_int(row.get("SV")),
                    "INV":                      to_int(row.get("INV")),
                    "INV_Condition":            clean(row.get("INV_Condition")),
                    "FNP":                      to_int(row.get("FNP")),
                    "FNP_Condition":            clean(row.get("FNP_Condition")),
                    "W":                        to_int(row.get("W")),
                    "LD":                       to_int(row.get("LD")),
                    "OC":                       to_int(row.get("OC")),
                    # Leader fields
                    "leader_ability_name":      clean(row.get("Leader Ability Name")),
                    "leader_eligible_units":    split_list(row.get("Leader Eligible Units", "")),
                    "co_leader_eligible_with":  split_list(row.get("Co-Leader Eligible With", "")),
                    "leader_restrictions":      clean(row.get("Leader Restrictions")),
                    # Reference lists (names only)
                    "unit_ability_names":       split_list(row.get("Unit Ability Names", "")),
                    "rule_names":               split_list(row.get("Rule Names", "")),
                    "keyword_names":            split_list(row.get("Keyword Names", "")),
                }
                model_groups.append(model_group_obj)

            # ----------------------------------------------------------
            # weapons
            # ----------------------------------------------------------
            weapons = []
            for row in weapons_rows:
                if (clean(row.get("Army Name")), clean(row.get("Unit Name"))) != (army_name, unit_name):
                    continue
                mg = clean(row.get("Model Group"))
                weapon_obj = {
                    "model_group":          mg,
                    "weapon_type":          clean(row.get("Weapon Type")),
                    "weapon_name":          clean(row.get("Weapon Name")),
                    "range":                clean(row.get("Range")),
                    "A":                    clean(row.get("A")),       # char: may be D6, 2D6+1 etc.
                    "BS":                   clean(row.get("BS")),
                    "WS":                   clean(row.get("WS")),
                    "S":                    to_int(row.get("S")),
                    "AP":                   to_int(row.get("AP")),
                    "D":                    clean(row.get("D")),       # char: may be D3, D6+1 etc.
                    "weapon_ability_names": split_list(row.get("Weapon Ability Names", "")),
                    "weapon_keyword_names": split_list(row.get("Weapon Keyword Names", "")),
                    "is_base_equipment":    clean(row.get("Is Base Equipment")),
                }
                weapons.append(weapon_obj)

            # ----------------------------------------------------------
            # wargear_options
            # ----------------------------------------------------------
            wargear_options = []
            for row in wargear_rows:
                if (clean(row.get("Army Name")), clean(row.get("Unit Name"))) != (army_name, unit_name):
                    continue
                wargear_obj = {
                    "model_group":                  clean(row.get("Model Group")),
                    "models_per_trigger":            to_int(row.get("Models Per Trigger")),
                    "max_substitutions_per_trigger": to_int(row.get("Max Substitutions Per Trigger")),
                    "weapon_replaced":               clean(row.get("Weapon Replaced")),
                    "replacement_weapon_name":       clean(row.get("Replacement Weapon Name")),
                    "points_cost":                   to_int(row.get("Points Cost")),
                    "is_choice":                     clean(row.get("Is Choice")),
                    "option_group":                  clean(row.get("Option Group")),
                }
                wargear_options.append(wargear_obj)

            # ----------------------------------------------------------
            # other_options
            # ----------------------------------------------------------
            other_options = []
            for row in other_rows:
                if (clean(row.get("Army Name")), clean(row.get("Unit Name"))) != (army_name, unit_name):
                    continue
                other_obj = {
                    "model_group":          clean(row.get("Model Group")),
                    "option_name":          clean(row.get("Option Name")),
                    "points_cost":          to_int(row.get("Points Cost")),
                    "max_per_unit":         to_int(row.get("Max Per Unit")),
                    "carrier_notes":        clean(row.get("Carrier Notes")),
                    "ability_name":         clean(row.get("Ability Name")),
                    "is_mutually_exclusive": clean(row.get("Is Mutually Exclusive")),
                    "exclusion_group":      clean(row.get("Exclusion Group")),
                }
                other_options.append(other_obj)

            # ----------------------------------------------------------
            # points (flat object, keyed by army + unit name only)
            # ----------------------------------------------------------
            points_row = points_by_key.get((army_name, unit_name))
            if points_row:
                # Size slots: ordered, trailing-blank (D27). Build a list of the
                # size brackets that are actually populated, in order.
                size_slots = [
                    to_int(points_row.get("Size_1")),
                    to_int(points_row.get("Size_2")),
                    to_int(points_row.get("Size_3")),
                ]

                # Points matrix: 3 size rows x 3 unit-copy columns (D28).
                # Stored as a list of size-bracket entries, each carrying the
                # size and the per-copy costs (1st, 2nd, 3rd+ unit).
                # Only emit an entry for a size slot that is populated, so the
                # JSON mirrors the trailing-blank size convention.
                size_entries = []
                for idx, slot in enumerate(["1", "2", "3"]):
                    size_value = size_slots[idx]
                    if size_value is None:
                        continue  # trailing blank size slot — skip
                    size_entries.append({
                        "size":          size_value,
                        "first_unit":    to_int(points_row.get(f"Points_{slot}-1")),
                        "second_unit":   to_int(points_row.get(f"Points_{slot}-2")),
                        "third_plus":    to_int(points_row.get(f"Points_{slot}-3")),
                    })

                points = {
                    "sizes": size_entries,
                }
            else:
                print(f"  WARNING: No points data found for '{army_name}' / '{unit_name}'")
                points = None

            # ----------------------------------------------------------
            # abilities (embedded in full, per model group)
            # ----------------------------------------------------------
            abilities = []
            for row in abilities_rows:
                if (clean(row.get("Army Name")), clean(row.get("Unit Name"))) != (army_name, unit_name):
                    continue
                ability_obj = {
                    "model_group":       clean(row.get("Model Group")),
                    "ability_name":      clean(row.get("Unit Ability Name")),
                    "ability_description": clean(row.get("Unit Ability Description")),
                }
                abilities.append(ability_obj)

            # ----------------------------------------------------------
            # bundled_swaps (merged from bundled_swaps.json, keyed army+unit)
            # ----------------------------------------------------------
            bundled_swaps = bundles.get((army_name, unit_name))

            # A bundle that manages a weapon family OWNS that slot; drop any flat
            # wargear_option whose replaced family the bundle already removes
            # (scoped to model group), so the same swap isn't offered twice.
            if bundled_swaps:
                removed_by_mg = {}
                for grp in bundled_swaps:
                    gmg = grp.get("model_group") or "All"
                    bag = removed_by_mg.setdefault(gmg, set())
                    for ep in grp.get("endpoints", []):
                        for rem in ep.get("removes", []):
                            bag.add(weapon_base(rem))

                def _bundle_owns(wo):
                    rb = weapon_base(wo.get("weapon_replaced"))
                    if not rb:
                        return False
                    wmg = wo.get("model_group") or "All"
                    for gmg, bag in removed_by_mg.items():
                        if (gmg == "All" or gmg == wmg) and rb in bag:
                            return True
                    return False

                kept = [wo for wo in wargear_options if not _bundle_owns(wo)]
                dropped = len(wargear_options) - len(kept)
                if dropped:
                    print(f"  {army_name}/{unit_name}: suppressed {dropped} "
                          f"wargear option(s) owned by a bundle.")
                wargear_options = kept

            # ----------------------------------------------------------
            # Assemble unit object
            # ----------------------------------------------------------
            unit_obj = {
                "unit_name":      unit_name,
                "unit_type":      unit_type,
                "model_groups":   model_groups,
                "weapons":        weapons,
                "wargear_options": wargear_options,
                "other_options":  other_options,
                "bundled_swaps":  bundled_swaps,
                "points":         points,
                "abilities":      abilities,
            }

            army_obj["units"].append(unit_obj)

        output.append(army_obj)

    return output


